struct Tile {
	glm::vec2 position;
	aie::Texture* texture;
	bool passable;
	float cost;
	void Draw(aie::Renderer2D* r) {
		r->drawSprite(texture, position.x, position.y);
	}
	
};

struct PassableTile : Tile{
	PassableTile(glm::vec2 pos, float c = 1) {
		cost = c;
		passable = true;
		position = pos;
		texture = new aie::Texture("./bin/textures/Map_Passable.png");
	}

};
struct ImpassableTile : Tile {
	ImpassableTile(glm::vec2 pos) {
		passable = false;
		position = pos;
		texture = new aie::Texture("./bin/textures/Map_Impassable.png");
	}
};

